package uk.ac.cam.cl.lm649.bonjourtesting.crypto;

public class KeyDecodingException extends Exception {

    public KeyDecodingException(String message) {
        super(message);
    }

    public KeyDecodingException(Throwable cause) {
        super(cause);
    }

    public KeyDecodingException(String message, Throwable cause) {
        super(message, cause);
    }

    public KeyDecodingException() {
        super();
    }

}
